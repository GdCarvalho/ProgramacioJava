
public class BucleWhile {

	public static void main(String[] args) {
		//Programa que muestra por pantalla los 20 primeros numeros
		
		int i;
		// inicializar a 1
		
		i=1;
		
		while(i<=20) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println();
		System.out.println("Fin del bucle");

	}

}
